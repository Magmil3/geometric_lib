def area(a, b, c):
    '''
     //Функция находит полупериметер треугольника с переданными сторонами
        Пример вызова функции: 
        input: area(7, 7, 7)
        output: 10,5//
    '''
    return (a + b + c) / 2


def perimeter(a, b, c):
    '''
     //Функция находит периметер треугольника с переданными сторонами
        Пример вызова функции: 
        input: perimeter(7, 7, 7)
        output: 21//
    '''
    return a + b + c